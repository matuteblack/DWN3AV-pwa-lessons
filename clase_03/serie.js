class Serie {
    title  = '';
    imgUrl = '';
    year   = '';

    constructor() {

    }

    setTitle(title) {
        this.title = title;
    }
    getTitle() {
        return this.title;
    }

    setImgUrl(imgUrl) {
        this.imgUrl= imgUrl;
    }
    getImgUrl() {
        return this.imgUrl;
    }

    setYear() {
        this.year = year;
    }
    getYear() {
        return this.year;
    }
}